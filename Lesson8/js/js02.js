// Toán tử
// Toán tử số học: *; /; +; - ; % ; ++; --
var a = 3;
var b = 4;
console.log("a=",a,"; b=",b);
var result = a*b;
console.log(a,"*",b,"=",result);

result = a/b;
console.log(a,"/",b,"=",result);

result = a+b;
console.log(a,"+",b,"=",result);

result = a-b;
console.log(a,"-",b,"=",result);

result = a%b;
console.log(a,"%",b,"=",result);
//++
var res = a++;//sau câu lệnh thì giá trị của a mới tăng
console.log(a,"++ => ", res);
res = b++;// tăng gb lên 1 đơn vị sau đó mới gán cho res
console.log("++",b,"=>",res);

var res = a--;
console.log(a,"-- => ", res);
res = b--;
console.log("--",b,"=>",res);






